export type I18nKey = keyof (typeof dictionary)['en'];

export function useI18n(language: Language) {
  return useMemo(() => {
    const selected = dictionary[language] ?? dictionary.de;

    const t = (key: I18nKey): string => {
      return selected[key] ?? dictionary.en[key] ?? key;
    };

    return { t };
  }, [language]);
}
